    private void agregarAfiliadoCopere() {
        try {
            Connection con = Conexion.getConnection();
            String sql = "INSERT INTO [PlanillaCopere]\n"
                    + "           ([Mes]\n"
                    + "           ,[Cod_Descuento]\n"
                    + "           ,[Num_Admin]\n"
                    + "           ,[Monto]\n"
                    + "           ,[Nro_Cuota]\n"
                    + "           ,[Total_Cuota]\n"
                    + "           ,[FechaIngreso])\n"
                    + "     VALUES\n"
                    + "           (?\n"
                    + "           ,'8070'\n"
                    + "           ,?\n"
                    + "           ,?\n"
                    + "           ,1\n"
                    + "           ,1\n"
                    + "           ,GETDATE());\n";
            PreparedStatement pstm = con.prepareStatement(sql);
            pstm.setString(1, CargaCopere.jYearChooser3.getYear() + "" + (CargaCopere.jMonthChooser3.getMonth()+1));
            pstm.setString(2, jTextField2.getText());
            pstm.setString(3, jTextField3.getText());
            
            pstm.executeUpdate();
            
            JOptionPane.showMessageDialog(null, "Se ha guardado correctamente la informaci√≥n");
            
        } catch (SQLException ex) {
            Logger.getLogger(RegistroAfiliadoCopere.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }
