    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int selectedRow = tbresultado.getSelectedRow();
        if (selectedRow != -1) { // Verificar si hay una fila seleccionada
            // Mostrar un cuadro de diÃ¡logo de confirmaciÃ³n
            int confirm = JOptionPane.showConfirmDialog(this, "Â¿EstÃ¡s seguro de que quieres eliminar esta fila?",
                    "Confirmar EliminaciÃ³n", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Eliminar la fila seleccionada del modelo de datos
                eliminarCuenta(tbresultado.getValueAt(selectedRow, 0).toString());
                modelo.removeRow(selectedRow);
            }
        } else {
            // Si no hay fila seleccionada, mostrar un mensaje de advertencia
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una fila para eliminar.",
                    "Fila no Seleccionada", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed
    private void mostrarCuenta(String Cuenta) {
        try {
            Connection cnn = Conexion.getConnection();
            String sql = "SELECT Cuenta, Cuenta_Equivalente FROM LibroMayorEquivalencias WHERE Cuenta = ?";

            try (PreparedStatement pst = cnn.prepareStatement(sql)) {
                pst.setString(1, Cuenta);
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    String cuentaEquivalente = rs.getString("Cuenta_Equivalente");

                    if (cuentaEquivalente != null && !cuentaEquivalente.isEmpty()) {
                        // Si hay cuenta equivalente, mostrar mensaje
                        JOptionPane.showMessageDialog(null, "Esta cuenta ya tiene una cuenta equivalente.", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                        txtcuenta.requestFocus();
                        txtcuenta.selectAll();
                    } else {
                        // Si no hay cuenta equivalente, habilitar el campo
                        txtcuentae.setText("");
                        txtcuentae.requestFocus();
                    }
                } else {
                    // Limpiar los campos si no se encuentra la cuenta
                      txtcuentae.requestFocus();
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace(); // Manejo de excepciones
        }
    }
